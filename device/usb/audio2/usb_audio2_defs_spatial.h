#ifndef USB_AUDIO2_DEFS_SPATIAL_H
#define USB_AUDIO2_DEFS_SPATIAL_H

#define SP_FL	BIT(0)	// Front Left
#define SP_FR	BIT(1)	// Front Right
#define SP_FC	BIT(2)	// Front Center
#define SP_LFE	BIT(3)	// Low Frequency Effects
#define SP_BL	BIT(4)	// Back Left
#define SP_BR	BIT(5)	// Back Right
#define SP_FLC	BIT(6)	// Front Left of Center
#define SP_FRC	BIT(7)	// Front Right of Center
#define SP_BC	BIT(8)	// Back Center
#define SP_SL	BIT(9)	// Side Left
#define SP_SR	BIT(10)	// Side Right
#define SP_TC	BIT(11)	// Top Center
#define SP_TFL	BIT(12)	// Top Front Left
#define SP_TFC	BIT(13)	// Top Front Center
#define SP_TFR	BIT(14)	// Top Front Right
#define SP_TBL	BIT(15)	// Top Back Left
#define SP_TBC	BIT(16)	// Top Back Center
#define SP_TBR	BIT(17)	// Top Back Right
#define SP_TFLC	BIT(18)	// Top Front Left of Center
#define SP_TFRC	BIT(19)	// Top Front Right of Center
#define SP_LLFE	BIT(20)	// Left Low Frequency Effects
#define SP_RLFE	BIT(21)	// Right Low Frequency Effects
#define SP_TSL	BIT(22)	// Top Side Left
#define SP_TSR	BIT(23)	// Top Side Right
#define SP_BTC	BIT(24)	// Bottom Center
#define SP_BLC	BIT(25)	// Back Left of Center
#define SP_BRC	BIT(26)	// Back Right of Center
#define SP_RD	BIT(31)	// Raw Data

#endif // USB_AUDIO2_DEFS_SPATIAL_H
